#include "../minishell.h"
#include "../utils/ft_utils.h"

int	check_pwd_options(char *args);
int	print_pwd(void);

int	builtin_pwd(char *args)
{
	int	ret;

	ret = check_pwd_options(args);
	if (ret != 0)
		return (ret);
	return (print_pwd());
}
